# Boolean
print(True and True)
print(True and False)
print(False and False)
print(True or False)

# comparison
print(100 == 100)
print(100 != 100)
print(100 != 50)
print(70 == 50)
print(300 >= 300)
print(300 <= 500)

print(100 > 99 or 5 != 5)

v, v2, v3 = "John", "finn", "sean"

print('o' in v)
print('a' not in v2)
print('sea' in v3)

# Chain Comparison Operator
print(11 <= 22 != 4 and 77 > 43 < 45)
print(11 <= 22 == 4 and 77 > 43 < 45)

print('j' in v and 'inn' in v2 or 7 >= 4 != 5 and 200 < 201)
